#!/usr/bin/env python3
""" File that contains a concatenation string function """


def concat(str1:str, str2:str) -> str:
    """
    Description:
        Function that returns a concatenated string

    Args:
        str1 (str): first number
        str2 (str): second number

    Returns:
        a concatenated string
    """
    return str1 + str2
